package org.codehaus.enunciate.samples.genealogy.data;

/**
 * Enumeration for event types.
 *
 * @author Ryan Heaton
 */
public enum EventType {

  /**
   * Specifies a birth event.
   */
  birth,

  /**
   * Specifies a christening event.
   */
  christening,

  /**
   * Specifies a marriage event.
   */
  marriage,

  /**
   * Specifies a death event.
   */
  death,

  /**
   * Specifies a burial event.
   */
  burial

}
